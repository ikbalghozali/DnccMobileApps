package Kotlin

fun main() {
    val name = "Budi"
    var age = 17
    val height = 171.1
    println("Name : $name, age : $age, height : $height")
    age=18
    println("Name : $name, age : $age, height : $height")

    val num1:Int = 1
    val num2:Double = 3.14
    val num3:Float = 1.23792f
    val num4:Long = 13812764
    println("Integer : $num1")
    println("Double : $num2")
    println("Float : $num3")
    println("Long : $num4")

    val letter:Char = 'A'
    val string:String = "This is String"
    println("Char : $letter")
    println("String : $string")

}